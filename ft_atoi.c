#include "libft.h"

int	ft_atoi(const char *str)
{
	size_t	results;
	int		sign;

	results = 0;
	sign = 1;
	while (ft_isspace(*str))
		str++;
	if (*str == '-' || *str == '+')
	{
		if (*str == '-')
			sign *= -1;
		str++;
	}
	while (ft_isdigit(*str))
	{
		results = results * 10 + *str - 48;
		str++;
	}
	return (results * sign);
}
